struct IconView: View
{
    @State private var currentPage = 0 // 当前页数
    
    let row = 6
    let column = 10
    
    var body: some View
    {
        VStack
        {
            VStack
            {
                ForEach(0 ..< 10)
                {
                    rowNum in
                    HStack
                    {
                        ForEach(0 ..< 6)
                        {
                            num in
                            Button(
                                action:
                                    {
                                        let name = emojiIconList[currentPage*180 + rowNum*10 + num]
                                        print(name)
                                        ARPeer.sendData(data: name, type: "emoji")
                                        globalThreeMeterUIModel.showSendEmoji.toggle()
                                    }
                            )
                            {
                                Image(uiImage:
                                UIImage(named: "Images/\(emojiIconList[currentPage*60 + rowNum*row + num]).png")
                                      ??
                                (UIImage(named: "openAI.png")!)
                                )
                                    .resizable()
                                    .frame(width: 50, height: 50)
                            }
                        }
                    }
                }
            }
            
            Spacer()
            
            HStack {
                Button(action: {
                    if currentPage > 0 {
                        currentPage -= 1
                    }
                    else
                    {
                        currentPage = 29
                    }
                })
                {
                    Image(systemName: "chevron.left")
                }
                Spacer()
                Text("\(currentPage + 1)")
                Spacer()
                Button(action: {
                    if currentPage < 29
                    {
                        currentPage += 1
                    }
                    else
                    {
                        currentPage = 0
                    }
                })
                {
                    Image(systemName: "chevron.right")
                }
            }
        }
    }
}

